#Implicit : Automatic conversion of data types by python iteself is called Implicit Type Casting

x=1
print(type(x))

x=4.0
print(type(x))

y=x*x
print(type(y))



#Explicit: Manual Types casting done by user is called explicit type casting

a=12.4
print(type(int(a)))


a=12.4
print(type(str(a)))
